# Função auxiliar para adicionar a seta de perturbação
def add_perturbation_arrow(ax, time_vec, data_vec, t_start):
    # Encontra o índice no vetor de tempo mais próximo do tempo de início
    idx_p = (np.abs(time_vec - t_start)).argmin()

    ax.annotate('',
                 xy=(time_vec[idx_p], data_vec[idx_p]), xycoords='data', # Aponta para o dado
                 xytext=(0, 32), textcoords='offset points', # Texto deslocado para cima
                 fontsize=9, color='red', ha='center',
                 arrowprops=dict(arrowstyle="->", color='red', linewidth=1.3))


